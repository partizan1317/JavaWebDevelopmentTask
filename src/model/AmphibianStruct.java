package model;

public class AmphibianStruct {
	
	private String genus;
	private String type;
	
	public AmphibianStruct(String genus, String type) {
		this.genus = genus;
		this.type = type;
	}
	
	public String getGenus() {
		return genus;
	}
	
	public String getType() {
		return type;
	}

}
